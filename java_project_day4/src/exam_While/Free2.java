package exam_While;

public class Free2 {

	public static void main(String[] args) {
	
		int a;
		int b;
		for (a = 0; a <= 15; a++) {
			for (b = 0; b <= 15; b++) {
			if(a > b) {
				System.out.print("*"); 
			}
		}
			System.out.println(); 
		}
		int c;
		int d;
		for (c = 15; c >= 0; c--) {
			for (d = 15; d >= 0; d--) {
			if(c > d) {
				System.out.print("*"); 
			}
		}
			System.out.println(); 
		}
		
		
		
		
		
		
		
		
		
		
		
	}
}